
soma = 5+2
subtracao = 5-2
multiplicacao = 5*2
divisao = 4/3
potencia = 5**2
divisaoInteira = 5//2
resto = 5%2

print(f"A soma de 5 + 2 = {soma}")
print(f"A subtraçao de 5 - 2 = {subtracao}")
print(f"A multiplicaçao de 5 * 2 = {multiplicacao}")
print(f"A divisao de 5 / 2 = {divisao:.2f}")
print(f"A potencia de 5 ** 2 = {potencia}")
print(f"A divisao inteira de 5 // 2 = {divisaoInteira}")
print(f"O resto de 5 % 2 = {resto}")

