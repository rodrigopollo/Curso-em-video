
# Crie um script Python que leia dia, nome e ano de nascimento de uma pessoa
# e mostre uma mensagem com a data formatada.

dia = input('Qual dia voce nasceu?')
mes = input('Qual mes voce nasceu?')
ano = input('Qual ano voce nasceu?')

print(dia + '/' + mes + '/' + ano + '.')
print(f"Voce nasceu no dia {dia} de {mes} do ano de {ano}")